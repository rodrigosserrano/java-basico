public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        // Definindo um double
        double salarioMinimo = 2500.50;

        // Definindo um float, é necessário adicionar um F no final para que o java não faça a conversão para double
        float salarioMinimo2 = 2500.50F;

        // Definir um long porecisa setar um L no final para que o Java nao converta para int
        long numero2 = 100L;

        // inteiro
        int inteiro = 2;

        // Fazer um casting de uma variável
        short inteiroCasting = (short) inteiro;

        // Constante em java | Deve adicionar 'final', por convenção a varíavel deve ser escrito em maiúsculo
        final int NUMERO = 245;
    }
}
